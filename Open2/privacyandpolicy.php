    <h1 style="text-align: center;">Kebijakan Privasi Platform Masa-Blog</h1>

    <p><b>Selamat datang di Masa-Blog!</b> Platform kami menyediakan wadah bagi para penulis untuk berbagi ide,
        membangun komunitas, dan mengekspresikan diri melalui tulisan. Kami berkomitmen untuk melindungi privasi
        pengguna kami dan memastikan bahwa informasi mereka digunakan secara bertanggung jawab. Kebijakan Privasi ini
        menjelaskan bagaimana kami mengumpulkan, menggunakan, dan mengungkapkan informasi pribadi Anda.</p>

    <h2 style="text-align: center;">1. Informasi yang Kami Kumpulkan</h2>

    <p>Kami mengumpulkan informasi pribadi dari Anda ketika Anda membuat akun, menggunakan layanan kami, atau
        berkomunikasi dengan kami. Informasi ini mungkin termasuk:</p>

    <ul>
        <li>Nama dan alamat email</li>
        <li>Kata sandi</li>
        <li>Alamat IP</li>
        <li>Perangkat dan browser yang Anda gunakan</li>
        <li>Konten yang Anda posting dan komentar yang Anda buat</li>
        <li>Aktivitas Anda di platform kami</li>
    </ul>

    <p>Kami juga mengumpulkan informasi non-pribadi tentang Anda, seperti data penggunaan dan demografi. Informasi ini
        digunakan untuk meningkatkan layanan kami dan memberikan pengalaman yang lebih personal bagi Anda.</p>

    <h2 style="text-align: center;">2. Bagaimana Kami Menggunakan Informasi Anda</h2>

    <p>Kami menggunakan informasi Anda untuk:</p>

    <ul>
        <li>Memberikan layanan kami kepada Anda</li>
        <li>Mengelola akun Anda</li>
        <li>Mengirimkan komunikasi kepada Anda, seperti pemberitahuan dan pembaruan</li>
        <li>Meningkatkan layanan kami</li>
        <li>Menganalisis data penggunaan</li>
        <li>Mencegah penipuan dan penyalahgunaan</li>
    </ul>

    <h2 style="text-align: center;">3. Pembagian Informasi Anda</h2>

    <p>Kami tidak akan membagikan informasi pribadi Anda kepada pihak ketiga tanpa persetujuan Anda. Namun, kami mungkin
        membagikan informasi Anda dengan penyedia layanan pihak ketiga yang membantu kami menjalankan layanan kami,
        seperti penyedia hosting dan layanan email. Kami hanya akan membagikan informasi yang diperlukan untuk penyedia
        layanan pihak ketiga untuk melakukan layanan mereka.</p>

    <p>Kami juga dapat mengungkapkan informasi Anda jika diwajibkan oleh hukum atau jika kami yakin bahwa pengungkapan
        tersebut diperlukan untuk melindungi hak atau keselamatan kami atau orang lain.</p>

    <h2 style="text-align: center;">4. Keamanan Informasi Anda</h2>

    <p>Kami mengambil langkah-langkah keamanan yang wajar untuk melindungi informasi pribadi Anda dari kehilangan,
        pencurian, akses yang tidak sah, pengungkapan, perubahan, atau perusakan. Namun, tidak ada metode keamanan yang
        sempurna, dan kami tidak dapat menjamin keamanan absolut dari informasi Anda.</p>

    <h2 style="text-align: center;">5. Pilihan Anda</h2>

    <p>Anda dapat mengakses dan memperbarui informasi pribadi Anda di pengaturan akun Anda. Anda juga dapat meminta kami
        untuk menghapus informasi pribadi Anda dengan menghubungi kami di info@uin-malang.ac.id.</p>

    <h2 style="text-align: center;">6. Perubahan Kebijakan Privasi Ini</h2>

    <p>Kami dapat mengubah Kebijakan Privasi ini sewaktu-waktu. Kami akan memberi tahu Anda tentang perubahan tersebut
        melalui email atau dengan memposting pemberitahuan di platform kami.</p>

